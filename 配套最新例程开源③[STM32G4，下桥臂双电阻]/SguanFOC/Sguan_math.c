// 常量宏定义声明
#define Value_512_2PI       81.487330863050417f

// 数值max判断函数
float Value_maxf(float a, float b){
    return (a > b) ? a : b;
}

// 数值min判断函数
float Value_minf(float a, float b){
    return (a < b) ? a : b;
}

// 数值fabsf绝对值函数
float Value_fabsf(float x){
  union{
    float f;
    uint32_t i;
  }u;
  u.f = x;
  u.i &= 0x7FFFFFFF;
  return u.f;
}

// 数值sqrtf开根号函数
float Value_sqrtf(float x){
    if (x <= 0.0f) return 0.0f;
    float y = x;
    int i = *(int*)&y;
    i = 0x5f3759df - (i >> 1);
    y = *(float*)&i;
    
    y = y * (1.5f - 0.5f*x*y*y);
    y = y * (1.5f - 0.5f*x*y*y);
    
    return y * x;  // (1/sqrt(x)) * x = sqrt(x)
}

// 数值限幅float函数
void Value_Limit(float *val, float max, float min){
    if (*val > max) *val = max;
    if (*val < min) *val = min;
}

// 数值区间权重获取函数
float Value_Gain_Get(float *gain, 
                float real_speed,
                float abs_max,
                float abs_min){
    float abs_real = Value_fabsf(real_speed);

    if (abs_real <= abs_min){
        *gain = 0.0f;
    }
    else if (abs_real >= abs_max){
        *gain = 1.0f;
    }
    else{
        *gain = (abs_real - abs_min)/(abs_max - abs_min);
    }
    return abs_real;
}

// 参数取模[0, 2π)
float Value_normalize(float angle){
    while (1){
        if (angle > Value_2PI)
            angle -= Value_2PI;
        else if (angle < 0)
            angle += Value_2PI;
        else
            break;
    }
    return angle;
}

// 数值角度越位处理函数
void Value_Correct(float *angle, float error){
    if (error >= Value_PI){
        *angle -= Value_2PI;
    }
    if (error <= -Value_PI){
        *angle += Value_2PI;
    }
}

// 数值限定set函数
int8_t Value_set(int8_t val, int8_t max, int8_t min){
    if (val > max) return 0;
    if (val < min) return 0;
    return val;
}

// 数值计算的Sign符号函数
float Value_Sign(float value){
    if (value > 0){
        return 1.0f;
    }
    else{
        return -1.0f;
    }
}

// 电机角度积分函数
void Value_Rad_Loop(float *Output, 
                float Input, 
                float Last_in, 
                float T){
    *Output += (Input + Last_in)*(T/2.0f);
    *Output = Value_normalize(*Output);
}

// 数值Hz->Rad/s或者n->Rad
float Value_Rad_from_Hz(float hz){
    return hz * Value_2PI;
}

// 数值Rad/s->Hz或者Rad->n
float Value_Rad_to_Hz(float rad_s){
    return rad_s / Value_2PI;
}

// 克拉克变换
void clarke(float *i_alpha, 
        float *i_beta, 
        float i_a, 
        float i_b){
  *i_alpha = i_a;
  *i_beta = (i_a + 2*i_b)*Value_INV_SQRT3;
}

// 帕克变换
void park(float *i_d, 
        float *i_q, 
        float i_alpha, 
        float i_beta, 
        float sine, 
        float cosine){
  *i_d = i_alpha*cosine + i_beta*sine;
  *i_q = i_beta*cosine - i_alpha*sine;
}

// 帕克逆变换
void ipark(float *u_alpha, 
        float *u_beta, 
        float u_d, 
        float u_q, 
        float sine, 
        float cosine){
  *u_alpha = u_d*cosine - u_q*sine;
  *u_beta = u_q*cosine + u_d*sine;
}

