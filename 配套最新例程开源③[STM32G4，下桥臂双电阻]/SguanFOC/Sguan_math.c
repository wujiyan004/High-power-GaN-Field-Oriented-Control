// 快速正弦算法sine
float fast_sin(float theta){
    while (1){
        if (theta > Value_2PI)
            theta = theta - Value_2PI;
        else if (theta < 0)
            theta = theta + Value_2PI;
        else
            break;
    }
    float idx_f = theta * Value_512_2PI;
    int idx = (int)idx_f;
    
    if (idx >= 512) idx = 511;
    if (idx < 0) idx = 0;
    return sin_tab[idx];
}

// 快速求解sine和cosine
void fast_sin_cos(float x, float *sin_x, float *cos_x){
  *sin_x = fast_sin(x);
  *cos_x = fast_cos(x);
}

